    private void jTable_petugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_petugasMouseClicked
        // TODO add your handling code here:
        Button_Create.setVisible(false);
        Button_Delete.setVisible(true);
        Button_Update.setVisible(true);
        int kolom = 0;
        kolom = jTable_petugas.getSelectedRow();
        TextField_id.setText(jTable_petugas.getValueAt(kolom, 0).toString());
        TextField_password.setText(jTable_petugas.getValueAt(kolom, 4).toString());
        TextField_nama.setText(jTable_petugas.getValueAt(kolom, 1).toString());
        TextField_username.setText(jTable_petugas.getValueAt(kolom, 3).toString());
        if (jTable_petugas.getValueAt(kolom, 2).toString().equals("laki-laki")) {
            RadioButton_l.setSelected(true);
        } else {
            RadioButton_p.setSelected(true);
        }
        ComboBox_hakakses.setSelectedItem(jTable_petugas.getValueAt(kolom, 5).toString());
        TextArea_alamat.setText(jTable_petugas.getValueAt(kolom, 6).toString());
        TextField_id.setEnabled(false);
    }//GEN-LAST:event_jTable_petugasMouseClicked

