    private void Button_CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_CreateActionPerformed
        String gender = null;
        if (RadioButton_l.isSelected() == true) {
            gender = "laki-laki";
        } else {
            gender = "perempuan";
        }

        try {
//            pet.insertPetugas(TextField_id.getText(), TextField_nama.getText(), gender, TextField_username.getText(), TextField_password.getText(), ComboBox_hakakses.getSelectedItem().toString(), TextArea_alamat.getText());
//       JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan");

            if (TextField_id.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "ID Tidak Boleh Kososng");
            } else if (TextField_nama.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Nama Tidak Boleh Kosong");
            } else if (TextField_username.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "username tidak boleh kosong");
            } else if (TextField_password.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "password tidak boleh kosong");
            } else if (ComboBox_hakakses.getSelectedItem().toString().equals("")) {
                JOptionPane.showMessageDialog(null, "isi combocox");
            } else if (TextArea_alamat.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "isikan alamat dengan benar");
            } else {
                pet.insertPetugas(TextField_id.getText(), TextField_nama.getText(), gender, TextField_username.getText(), TextField_password.getText(), ComboBox_hakakses.getSelectedItem().toString(), TextArea_alamat.getText());
                JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan");
                jTable_petugas.setModel(pet.viewData());
            }
//       jTable_petugas.setModel(pet.viewData());
//       kosong();
        } catch (SQLException ex) {

            kosong();
            JOptionPane.showMessageDialog(rootPane, "GAGAL");
            Logger.getLogger(Petugas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Button_CreateActionPerformed

